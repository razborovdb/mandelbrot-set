package processing.sketches;

public class cNum {

    private float a;
    private float b;

    public cNum() {
        this.a = 0;
        this.b = 0;
    }

    public cNum(float a, float b) {
        this.a = a;
        this.b = b;
    }

    public float getA() {
        return a;
    }

    public void setA(float a) {
        this.a = a;
    }

    public float getB() {
        return b;
    }

    public void setB(float b) {
        this.b = b;
    }



}
